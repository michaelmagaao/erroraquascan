export function matchFishFromPredictions(predictions) {
  if (!predictions || predictions.length === 0) return FISH_DATABASE["default"];
  
  const fishKeywords = {
    "goldfish": ["goldfish", "gold fish", "carassius"],
    "shark": ["shark", "white shark", "great white", "tiger shark", "hammerhead"],
    "clownfish": ["clownfish", "clown fish", "nemo", "anemone fish"],
    "tuna": ["tuna", "bluefin", "yellowfin", "skipjack"],
    "salmon": ["salmon", "salmo", "atlantic salmon"],
    "trout": ["trout", "rainbow trout", "oncorhynchus"],
    "tilapia": ["tilapia", "oreochromis", "cichlid"],
    "carp": ["carp", "cyprinus", "koi"],
    "angelfish": ["angelfish", "angel fish", "pterophyllum"],
    "bass": ["bass", "largemouth", "smallmouth", "striped bass"]
  };

  for (const prediction of predictions) {
    const label = prediction.className.toLowerCase();
    for (const [fishKey, keywords] of Object.entries(fishKeywords)) {
      if (keywords.some(kw => label.includes(kw))) {
        return { ...FISH_DATABASE[fishKey], confidence: prediction.probability };
      }
    }
  }

  // Check for generic fish/aquatic terms
  const topPrediction = predictions[0];
  const label = topPrediction.className.toLowerCase();
  if (label.includes("fish") || label.includes("aquatic") || label.includes("marine") || label.includes("sea")) {
    return { ...FISH_DATABASE["default"], detectedAs: topPrediction.className, confidence: topPrediction.probability };
  }

  return { ...FISH_DATABASE["default"], detectedAs: topPrediction.className, confidence: topPrediction.probability };
}
